package swingEx4;

public class runnerDAO {
	private String name;
    private String teamName;
    private int g;
    private int sba;
    private int sb;
    private int cs;
    private float sbPer;
    private int oob;
    private int pko;
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTeamName() {
        return teamName;
    }

    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }

    public int getG() {
        return g;
    }

    public void setG(int g) {
        this.g = g;
    }

    public int getSba() {
        return sba;
    }

    public void setSba(int sba) {
        this.sba = sba;
    }

    public int getSb() {
        return sb;
    }

    public void setSb(int sb) {
        this.sb = sb;
    }

    public int getCs() {
        return cs;
    }

    public void setCs(int cs) {
        this.cs = cs;
    }

    public float getSbPer() {
        return sbPer;
    }

    public void setSbPer(float sbPer) {
        this.sbPer = sbPer;
    }

    public int getOob() {
        return oob;
    }

    public void setOob(int oob) {
        this.oob = oob;
    }

    public int getPko() {
        return pko;
    }

    public void setPko(int pko) {
        this.pko = pko;
    }
}
